
public class Problem_02 {

	public static void main(String[] args) {
		// check if given number is divided by 5 and 7 without remainder
		
		int num = 35;
		
		if (num % 5 == 0 & num % 7 == 0)
		{
			System.out.println("The number is divided by 5 and 7");
		}
		else
		{
			System.out.println("The number is not divided by 5 and 7");
		}

	}

}
